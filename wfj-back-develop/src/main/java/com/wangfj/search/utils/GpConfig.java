public class GpConfig {
    private String gpUrlTemplate;
    private String gpPath;
    private String gpGetList;
    private String gpAdd;

    public String getGpUrlTemplate() {
        return gpUrlTemplate;
    }

    public void setGpUrlTemplate(String gpUrlTemplate) {
        this.gpUrlTemplate = gpUrlTemplate;
    }

    public String getGpPath() {
        return gpPath;
    }

    public void setGpPath(String gpPath) {
        this.gpPath = gpPath;
    }

    public String getGpGetList() {
        return gpGetList;
    }

    public void setGpGetList(String gpGetList) {
        this.gpGetList = gpGetList;
    }

    public String getGpAdd() {
        return gpAdd;
    }

    public void setGpAdd(String gpAdd) {
        this.gpAdd = gpAdd;
    }
}
